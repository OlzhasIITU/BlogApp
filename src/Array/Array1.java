package Array;
// статичный массив
public class Array1 {
    public static void main(String[] args) {
        int[] arr1 = new int[4];
        arr1[0] = 1;
        arr1[1] = 2;
        arr1[2] = 3;
        arr1[3] = 4;

        for (int i = 0; i < arr1.length; i++) {
            System.out.print(arr1[i] + " ");
        }



    }
}
